package amu.zhcetstudent.data.model;

public class Subject {

    private String subjectRank;
    private String total;
    private String courseName;
    private String classAverage;
    private String course;
    private String grades;
    private String examMarks;
    private String highest;
    private String grace;
    private String sessionalMarks;

    public String getSubjectRank() {
        return subjectRank;
    }

    public void setSubjectRank(String subjectRank) {
        this.subjectRank = subjectRank;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getClassAverage() {
        return classAverage;
    }

    public void setClassAverage(String classAverage) {
        this.classAverage = classAverage;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getGrades() {
        return grades;
    }

    public void setGrades(String grades) {
        this.grades = grades;
    }

    public String getExamMarks() {
        return examMarks;
    }

    public void setExamMarks(String examMarks) {
        this.examMarks = examMarks;
    }

    public String getHighest() {
        return highest;
    }

    public void setHighest(String highest) {
        this.highest = highest;
    }

    public String getGrace() {
        return grace;
    }

    public void setGrace(String grace) {
        this.grace = grace;
    }

    public String getSessionalMarks() {
        return sessionalMarks;
    }

    public void setSessionalMarks(String sessionalMarks) {
        this.sessionalMarks = sessionalMarks;
    }

}